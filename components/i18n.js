// Hook: subscribes to window.__lang changes and returns t()
window.useT = function() {
  const [lang, setLang] = React.useState(window.__lang || "es");
  React.useEffect(() => {
    const handler = (e) => setLang(e.detail);
    window.addEventListener("langchange", handler);
    return () => window.removeEventListener("langchange", handler);
  }, []);
  const dict = window.I18N[lang] || window.I18N.es;
  return [(k) => dict[k] !== undefined ? dict[k] : k, lang];
};
